//calculadora.cpp
#include <math.h>

float soma (float n1, float n2){
	return n1+n2;
}

float subtracao (float n1, float n2){
	return n1-n2;
}

float multiplicacao (float n1, float n2){
	return n1*n2;
}

float divisao (float n1, float n2){
	return n1/n2;
}

float raiz (float n1){
	return sqrt(n1);
}

float potencia (float n1, float n2){
	return pow (n1, n2);
}
